# 常见转换语句 :
# int(x) : 将 x 转换成一个整数
# float(x) : 将 x 转换成一个浮点数
# str(x) : 将对象 x 转换为字符串
num_str = str(11)
print(type(num_str), num_str)

float_str = str(11.465)
print(type(float_str), float_str)

num = int("11")
print(type(num))

num1 = float("11.1")
print(type(num1))

